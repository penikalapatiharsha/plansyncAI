# backend/main.py
from fastapi import FastAPI, UploadFile, File, HTTPException
from fastapi.middleware.cors import CORSMiddleware
from dotenv import load_dotenv

from app.services.pdf_service import extract_text_from_pdf
from app.services.ai_service import parse_text_to_schema

load_dotenv()

app = FastAPI(title="PlanSync AI Engine", version="1.0.0")

# Enable CORS so your future React interface can call this API locally on your Mac
app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"], 
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

@app.get("/health")
def health_check():
    return {"status": "healthy", "service": "plansync-ai-core"}

@app.post("/api/v1/analyze-plan")
async def analyze_plan_document(file: UploadFile = File(...)):
    """
    Accepts PDF upload, extracts data, and serves the structured configuration JSON.
    """
    if not file.filename.endswith('.pdf'):
        raise HTTPException(status_code=400, detail="Invalid file type. Only PDF uploads are supported.")
        
    try:
        # Read file directly into an in-memory byte stream
        pdf_bytes = await file.read()
        
        # 1. Run Text Extraction
        filtered_text = extract_text_from_pdf(pdf_bytes)
        
        if not filtered_text.strip():
            raise HTTPException(status_code=422, detail="No relevant configuration sections identified in document.")
            
        # 2. Feed text blocks to AI schema mapper
        structured_config = parse_text_to_schema(filtered_text)
        
        return structured_config
        
    except Exception as e:
        raise HTTPException(status_code=500, detail=f"Internal Processing Error: {str(e)}")


