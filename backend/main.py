from fastapi import UploadFile, File, Form
from app.services.census_engine import cleanse_employee_census
import json

@app.post("/api/v1/cleanse-census")
async def upload_census_endpoint(
    file: UploadFile = File(...),
    extracted_rules_json: str = Form(...)  # Accepts the JSON string generated by the AI
):
    # Read the uploaded CSV data
    contents = await file.read()
    csv_text_stream = contents.decode("utf-8")
    
    # Convert the stringified frontend rules back into a native Python dictionary
    plan_rules = json.loads(extracted_rules_json)
    
    # Execute our automated auditing logic
    results = cleanse_employee_census(csv_text_stream, plan_rules)
    
    return results